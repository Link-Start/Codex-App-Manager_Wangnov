export const CATALOG: Record<Lang, Record<Key, string>> = {
  en: EN,
  "zh-CN": ZH,
  "zh-TW": ZH_TW,
  ja: JA,
  ko: KO,
  fr: FR,
  de: DE,
  es: ES,
  "pt-BR": PT_BR,
  ru: RU,
  ar: AR,
};

export interface LangMeta {
  code: Lang;
  /** The language's own endonym, shown in the language picker. */
  native: string;
  dir: "ltr" | "rtl";
}

// Display order in Settings. Arabic is the only RTL language for now.
export const LANGS: LangMeta[] = [
  { code: "en", native: "English", dir: "ltr" },
  { code: "zh-CN", native: "简体中文", dir: "ltr" },
  { code: "zh-TW", native: "繁體中文", dir: "ltr" },
  { code: "ja", native: "日本語", dir: "ltr" },
  { code: "ko", native: "한국어", dir: "ltr" },
  { code: "fr", native: "Français", dir: "ltr" },
  { code: "de", native: "Deutsch", dir: "ltr" },
  { code: "es", native: "Español", dir: "ltr" },
  { code: "pt-BR", native: "Português", dir: "ltr" },
  { code: "ru", native: "Русский", dir: "ltr" },
  { code: "ar", native: "العربية", dir: "rtl" },
];

export function dirOf(l: Lang): "ltr" | "rtl" {
  return LANGS.find((x) => x.code === l)?.dir ?? "ltr";
}

export type TFn = (key: Key, vars?: Record<string, string | number>) => string;

interface I18nCtx {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: TFn;
}

const Ctx = createContext<I18nCtx | null>(null);
const LS_KEY = "cam.lang";

const SUPPORTED = new Set<Lang>(LANGS.map((l) => l.code));

// Map any BCP-47 tag (e.g. "pt-BR", "zh-Hant-HK", "de-AT") to the closest
// supported language, or null if none fits. Exported for unit tests.
export function matchTag(tag: string): Lang | null {
  const t = tag.toLowerCase();
  if (!t) return null;
  if (t.startsWith("zh")) {
    return /(^|-)(tw|hk|mo|hant)(-|$)/.test(t) ? "zh-TW" : "zh-CN";
  }
  if (SUPPORTED.has(tag as Lang)) return tag as Lang; // exact, e.g. "pt-BR"
  const primary = t.split("-")[0];
  const hit = LANGS.find((l) => l.code.toLowerCase().split("-")[0] === primary);
  return hit ? hit.code : null;
}

// Pure selection logic behind detectLang, kept free of globals so it can be
// unit-tested: saved choice wins, else walk the preferred tags and take the
// first we support, else fall back to English.
export function pickLang(saved: string | null, prefs: readonly string[]): Lang {
  if (saved && SUPPORTED.has(saved as Lang)) return saved as Lang;
  for (const tag of prefs) {
    const m = matchTag(tag);
    if (m) return m;
  }
  return "en";
}

// Auto-detect from the live browser environment, delegating the choice to the
// testable, side-effect-free pickLang.
function detectLang(): Lang {
  const prefs =
    navigator.languages && navigator.languages.length > 0
      ? Array.from(navigator.languages)
      : [navigator.language ?? ""];
  return pickLang(localStorage.getItem(LS_KEY), prefs);
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(detectLang);

  const setLang = useCallback((l: Lang) => {
    localStorage.setItem(LS_KEY, l);
    document.documentElement.lang = l;
    document.documentElement.dir = dirOf(l);
    withViewTransition(() => setLangState(l));
  }, []);

  useEffect(() => {
    document.documentElement.lang = lang;
    document.documentElement.dir = dirOf(lang);
  }, [lang]);

  const t = useCallback<TFn>(
    (key, vars) => {
      let s = CATALOG[lang][key] ?? CATALOG.en[key] ?? key;
      if (vars) {
        for (const [k, v] of Object.entries(vars)) {
          // split/join, not String.replace: an interpolated value containing
          // "$&"/"$1" (e.g. a path or an error string) must land verbatim.
          s = s.split(`{${k}}`).join(String(v));
        }
      }
      return s;
    },
    [lang],
  );

  const value = useMemo(() => ({ lang, setLang, t }), [lang, setLang, t]);
  return <Ctx.Provider value={value}>{children}</Ctx.Provider>;
}

export function useI18n(): I18nCtx {
  const ctx = useContext(Ctx);
  if (!ctx) throw new Error("useI18n must be used within I18nProvider");
  return ctx;
}
